package com.java.typeCasting;

//Narrowing->casting higher data type to lowyer data type (automatic casting)

public class ExampleNarrowingCasting {

	public static void main(String[] args) {
		

		double myDouble =9.0;
	    int myInt = (int)myDouble;//manual casting
	    System.out.println(myInt);
	    System.out.println(myDouble);

	}

}
